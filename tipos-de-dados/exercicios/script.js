//   && - PREVALECE O FALSE
//           TRUE      FALSE
console.log(3 === 3 && 4 < 1);
//           TRUE      TRUE
console.log(3 === 3 && 4 > 1);


//   || - PREVALECE O TRUE
//           TRUE      FALSE
console.log(3 === 3 || 4 < 1);
//          FALSE      FALSE
console.log(3 != 3 || 4 === 1);


console.log("\n");


//    ! - MUDA O RESULTADO
//              TRUE      FALSE
console.log(! (3 === 3 && 4 < 1));
//              TRUE      TRUE
console.log(! (3 === 3 && 4 > 1));
//              TRUE      FALSE
console.log(! (3 === 3 || 4 < 1));
//             FALSE      FALSE
console.log(! (3 != 3 || 4 === 1));

console.log("\n");
